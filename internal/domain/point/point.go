// Package point определяет двумерные координаты в лабиринте.
//
// Пакет предоставляет простую структуру для представления координат точек
// в двумерном пространстве лабиринта.
package point

// Point представляет двумерную координату в лабиринте.
// Содержит целочисленные координаты x и y.
type Point struct {
	x int
	y int
}

// NewPoint создает новую точку с указанными координатами.
//
// Параметры:
//   - x: горизонтальная координата (столбец)
//   - y: вертикальная координата (строка)
//
// Возвращает новый экземпляр Point.
func NewPoint(x, y int) Point {
	return Point{x: x, y: y}
}

// X возвращает горизонтальную координату точки (столбец).
func (p Point) X() int {
	return p.x
}

// Y возвращает вертикальную координату точки (строка).
func (p Point) Y() int {
	return p.y
}
