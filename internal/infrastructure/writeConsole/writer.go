// Package writeConsole предоставляет реализацию вывода лабиринтов в консоль.
//
// Пакет содержит реализацию интерфейса MazeWriter, которая выводит
// строковое представление лабиринтов в стандартный вывод (консоль).
package writeConsole

import (
	"fmt"
)

// ConsoleWriter реализует вывод лабиринтов в консоль.
// Использует стандартный вывод для отображения лабиринтов пользователю.
type ConsoleWriter struct{}

// WriteMaze выводит строковое представление лабиринта в консоль.
//
// Параметры:
//   - maze: строковое представление лабиринта для вывода
//   - _: игнорируемый параметр (имя файла не используется для консольного вывода)
//
// Возвращает ошибку при проблемах с выводом в консоль.
func (c *ConsoleWriter) WriteMaze(maze, _ string) error {
	_, err := fmt.Println(maze)
	return err
}
